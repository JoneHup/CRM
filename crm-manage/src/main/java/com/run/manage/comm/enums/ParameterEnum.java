package com.run.manage.comm.enums;
/**
 * 
 * <p>Description:
 * 			[对应字典表 parameter--枚举类]
 * 			paramType: 为五个数字，第一个数字代表模块-- 1、消息中心；2、任务中心；3绩效中心；4、素材库；5、马甲库
 * 					   第二三个数字代表功能-- 如 01 表示消息类型下拉菜单 ； 02 表示消息按钮类型分类。。。
 * 					   第四五个数字代表具体分类-- 如 01 表示消息通知 ； 02 表示紧急消息。。。
 * 					   如 10101 表示 消息中心模块的下拉菜单的具体消息分类
 * 	</p>
 * Created on 2018年10月18日
 * @author:[tongrongbing]
 * Copyright (c) 2018 BJRUN
 */
public enum ParameterEnum {

	OPER_NOTICE_CLASSIFY("101","消息中心，消息分类明细"),
	OPER_TASK_CLASSIFY("201","任务中心，任务分类明细"),
	OPER_ACHIEVEMENT_CLASSIFY("301","绩效中心，任务分类明细"),
	OPER_MATERIA_CLASSIFY("401","素材库，任务分类明细"),
	OPER_VEST_CLASSIFY("501","马甲库，任务分类明细");

	public String paramType;
	public String paramDesc;

	private ParameterEnum(String paramType ,String paramDesc) {
		this.paramType = paramType;
		this.paramDesc = paramDesc;
	}


}