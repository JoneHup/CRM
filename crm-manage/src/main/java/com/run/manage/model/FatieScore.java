package com.run.manage.model;

/**
 * @Auther: qiangyu
 * @Date: 2018/10/22
 * @Description: 网评员发帖和评分的实体类
 */
public class FatieScore {
    //网评员账户名
    private String fatieUserAccount;
    //发帖标题
    private String fatieTitle;
    //发帖内容
    private String fatieDesc;
    //得分
    private String score;

    public String getFatieUserAccount() {
        return fatieUserAccount;
    }

    public void setFatieUserAccount(String fatieUserAccount) {
        this.fatieUserAccount = fatieUserAccount;
    }

    public String getFatieTitle() {
        return fatieTitle;
    }

    public void setFatieTitle(String fatieTitle) {
        this.fatieTitle = fatieTitle;
    }

    public String getFatieDesc() {
        return fatieDesc;
    }

    public void setFatieDesc(String fatieDesc) {
        this.fatieDesc = fatieDesc;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
